#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char*))
{
	size_t	i;
	size_t	s_len;

	i = 0;
	s_len = ft_strlen(s);
	if (i < s_len)
	{
		f(i, *s);
		i++;
		*s++;
	}
}
